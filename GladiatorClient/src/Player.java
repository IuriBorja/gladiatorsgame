public class Player {

    String name;
    int speed;
    int health;
    public int xPos;
    public int yPos;

    public Player(String name, int speed, int health, int xPos, int yPos) {
        this.name = name;
        this.speed = speed;
        this.health = health;
        this.xPos = xPos;
        this.yPos = yPos;
    }


}
